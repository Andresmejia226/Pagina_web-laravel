                 <p class="text-justify">  Entre los diferentes medios de comunicación, algunos tienen la preferencia de la población.</p>

<p class="text-justify"> Así como el Internet surgió con el desarrollo de la tecnología y avanzó considerablemente entre los favoritos, la TV y radio no dejaron de tener impacto en la sociedad. Dentro de diferentes contextos y necesidades, cada uno de esos medios atiende las demandas del público.</p>

<p class="text-justify"> Por eso, la manera de hacer acciones es adaptada para cada uno de esos canales, ya que eso interfiere en la eficiencia de las campañas.</p>

<p class="text-justify">También es importante entender que los medios de comunicación populares son los que la sociedad está acostumbrada a utilizar.</p>  

<p class="text-justify"> A continuación, te explicaremos un poco más sobre los principales canales, cómo es la adherencia y cómo la población utiliza estas alternativas.</p> 
<br>
<h3>Televisión </h3> 
<p class="text-justify"> La televisión fue uno de los inventos que más causó impacto en la sociedad y se muestra como un medio de comunicación eficiente y completo, que transmite información, publicidad y entretenimiento.</p> 

<p class="text-justify"> Para la sociedad, tener todo eso en un único medio generó un gran impacto y transformó a la televisión en un medio extremamente atractivo desde su inicio.</p>

<p class="text-justify"> Considerando algunos datos obtenidos en la Encuesta Nacional de Consumo de Contenidos Audiovisuales en Radio, Televisión e Internet en el 2017 en México, comprendemos lo siguiente:</p>

<p class="text-justify"> 1- 92% de los entrevistados dijeron tener televisión en su hogar;</p>
<p class="text-justify"> 2- los canales de televisión abierta son vistos por el 64% de la población mexicana;</p>
<p class="text-justify"> 3- el 56 % de los mexicanos cuenta con señal de televisión paga.</p>
<br>
<h3>Radio</h3> 
<p class="text-justify">La radio es uno de los medios de comunicación más antiguos. Sobre su importancia, ella ocupó un destacado lugar en el desarrollo de la sociedad.</p> 

<p class="text-justify"> Según la encuesta, en el 2017, el 38% de las personas entrevistadas declararon escuchar radio.</p> 

<p class="text-justify">Pese a no enganchar tanto por no tener imágenes, cumple un rol bastante importante. Este canal también une información, entretenimiento y publicidad, pero con un formato diferente.</p>  

<p class="text-justify">Sobre la información, se limita a momentos específicos del día, como en el tráfico en la mañana.</p>  

<p class="text-justify"> Sin embargo, el consumo de música en este canal ha disminuido notablemente, ya que tiene, en las plataformas streaming e Internet, una gran competencia.</p>  
<br>
<h3>Internet</h3> 
<p class="text-justify"> El avance de internet es claro y el estudio ofreció datos que comprueban que: el 43% de la población afirma que es su medio de comunicación preferido para consumir contenidos audiovisuales. Es probable que esto ocurra por la amplitud de posibilidades que ella ofrece.</p>

<p class="text-justify"> Explicar esto es sencillo, considerando que ella proporciona muchas ventajas y comodidades, algunas de estas son:</p>

<p class="text-justify">1- la agilidad de la información;</p> 
<p class="text-justify">2- la movilidad;</p> 
<p class="text-justify">3- el amplio acceso a los contenidos;</p> 
<p class="text-justify">4- la posibilidad de consumir cualquier material de interés personal.</p> 
<p class="text-justify"> Otra cuestión importante es el acceso cada vez más amplio y que no se limita a clases sociales. El Instituto Nacional de Estadística y Geografía presentó los datos de la Encuesta Nacional sobre Disponibilidad y Uso de Tecnologías de la Información en los Hogares 2017, en la que destacó que 71.3 millones de mexicanos tiene acceso a Internet.</p>


<p class="text-justify">
    <br>
    Los medios de comunicación son instrumentos utilizados en la sociedad contemporánea para informar y comunicar
    mensajes en versión textual, sonora, visual o audiovisual. Algunas veces son utilizados para comunicar de forma
    masiva, para muchos millones de personas, como es el caso de la televisión o los diarios impresos o digitales, y
    otras, para transmitir información a pequeños grupos sociales, como es el caso de los periódicos locales o
    institucionales.

    Todos los días, los individuos y las comunidades acceden a material informativo que describe, explica y analiza
    datos y acontecimientos políticos, sociales, económicos y culturales, tanto a nivel local como en el contexto
    global. Para comienzos del siglo XXI, y en casi todas sociedades que habitan el mundo, los periódicos, estaciones
    radiales y páginas web son ejemplos de la compleja naturaleza de los medios de comunicación. En nuestras sociedades,
    estos canales son esenciales para el establecimiento y desarrollo de todo proceso de interacción humana. Los medios
    de comunicación son la materialización física de la necesidad de relacionarse entre sí que tienen todos los humanos.
    Mediante ellos se describen situaciones y problemas propios de nuestra realidad y, en ocasiones, se plantean
    análisis que contribuyen a su discusión. Los medios de comunicación permiten establecer procesos de intercambio de
    conocimientos y debates de carácter social.

    A comienzos del siglo XXI, los medios de comunicación también constituyen una gran red de plataformas tecnológicas
    que excede los límites nacionales. Actualmente existen redes globales, nacionales, regionales y comunitarias,
    constituidas por periódicos, revistas, emisoras radiales, canales de televisión y páginas web. Cada una de estas
    plataformas define la materialidad de la información que se transmite. Por ejemplo, los periódicos publican
    diariamente artículos, columnas de opinión, infografías y fotografías; las emisoras de radio, en sus programas,
    cubren de manera exhaustiva los acontecimientos que están pasando en cualquier lugar y a cualquier hora; los canales
    de televisión, con sus noticieros, sus programas de discusión, sus telenovelas y sus partidos de fútbol, parecen
    traer la realidad a la casa de los televidentes.

    A pesar de que en estos tiempos nuestra relación con los medios de comunicación sea un asunto cotidiano y casi
    natural, pues a diario vemos, escuchamos, leemos y estamos en contacto con diversas plataformas informativas, es
    importante tener en cuenta que no existe una definición que englobe todo su significado. De acuerdo al contexto de
    discusión, los medios de comunicación adquieren objetivos, beneficios y usos diferentes.

    Para algunos sectores de la sociedad contemporánea, los medios de comunicación son la manera más eficaz y rápida de
    transmitir un mensaje. Por ejemplo, una página web es útil para publicar un contenido que necesite divulgación
    inmediata entre varios sectores de nuestra comunidad. Sin embargo, para otros sectores de la sociedad, estos mismos
    medios de comunicación son entendidos como una herramienta política o como un vehículo de manipulación social. En
    nuestros tiempos, el exceso de información que recibimos a diario le ha dado un nuevo valor a la capacidad y la
    eficiencia para transmitir un mensaje de manera masiva. Diferentes sectores de la sociedad son conscientes de este
    valor y por eso utilizan los medios de comunicación para hacerse escuchar o para imponer su visión de los problemas.
    Así también, hay sectores de la sociedad que se acercan a los medios de comunicación en busca del reflejo de un
    momento específico, es decir, que los utilizan como un documento histórico o como un dispositivo en el que se
    manifiesta lo positivo y lo negativo de una situación o de un contexto histórico determinados. Todas las
    definiciones anteriormente mencionadas hacen alusión a los diversos papeles que los medios cumplen en el mundo del
    siglo XXI. Lo cierto es que, hoy en día, los medios de comunicación son un poder innegable en la sociedad mundial
    que ha permitido que se los catalogue como el "cuarto poder".
